/**
* @brief Function implementing the LowTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartLowTask */
void StartLowTask(void const * argument)
{
  /* USER CODE BEGIN StartLowTask */
  /* Infinite loop */
	 for(;;)
  {
		 // Mutex güvenli şekilde alınır
		 if (xSemaphoreTake(SimpleMutex, portMAX_DELAY) == pdTRUE)
		     {
			 // Eğer ADC voltaj değerlerinden biri 1.35V'dan büyükse, GPIO çıkışı aktif edilir
		       if (ADC_Volts[0] > 1.35f || ADC_Volts[1] > 1.35f || ADC_Volts[2] > 1.35f)
		       {
		         HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_SET);
		       }
		       else
		       {
		    	   // Aksi takdirde GPIO çıkışı kapatılır
		         HAL_GPIO_WritePin(GPIOA, GPIO_PIN_5, GPIO_PIN_RESET);
		       }
			xSemaphoreGive(SimpleMutex);
			osDelay(100);//100 ms bekle
	    }
  }
  /* USER CODE END StartLowTask */
}

