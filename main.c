/**
* @brief Function implementing the HighTask thread.
* @param argument: Not used
* @retval None
*/
/* USER CODE END Header_StartHighTask */
void StartHighTask(void const * argument)
{
  /* USER CODE BEGIN StartHighTask */
	//HAL_ADC_Start_DMA(&hadc1,(uint32_t*) ADC_VAL,3);
  /* Infinite loop */
  for(;;)
  {
	  xSemaphoreTake(SimpleMutex, portMAX_DELAY);
	  if(isFinished ==1)//Eğer ADC dönüşümü tamamlandıysa
	  {  // ADC ham değerlerini volta çevir
		  for (int i = 0; i < ADC_BUFFER_SIZE; i++) {
		  ADC_Volts[i] = (ADC_VAL[i] * VOLT_REF) / 4095.0f;


	  }

	}
	  isFinished=0;
  xSemaphoreGive(SimpleMutex);// Mutex serbest bırak

		  osDelay(500);

  }
  /* USER CODE END StartHighTask */
}

